// === HELPER FUNCTIONS ===
// Get appropriate thumbnail for a channel
export const getChannelThumbnail = (channelId, category) => {
  // Try to get specific image first
  switch (category) {
    case 'sports':
      return SPORTS_IMAGES[channelId] || FALLBACK_IMAGES.sports;
    case 'cinema':
      return CINEMA_IMAGES[channelId] || FALLBACK_IMAGES.cinema;
    case 'series':
      return SERIES_IMAGES[channelId] || FALLBACK_IMAGES.series;
    case 'news':
      return NEWS_IMAGES[channelId] || FALLBACK_IMAGES.news;
    case 'documentaries':
      return DOCUMENTARIES_IMAGES[channelId] || FALLBACK_IMAGES.documentaries;
    case 'kids':
      return KIDS_IMAGES[channelId] || FALLBACK_IMAGES.kids;
    case 'music':
      return MUSIC_IMAGES[channelId] || FALLBACK_IMAGES.music;
    default:
      return FALLBACK_IMAGES.default;
  }
};

// Get channel logo with fallback
export const getChannelLogo = (channelId) => {
  return LOGO_IMAGES[channelId] || FALLBACK_IMAGES.default;
};

